function(buffers) {
    var size = 0;
    for (var i = 0; i < buffers.length; i++) {
        size += buffers[i].length;
    }
    var concatenated = new Buffer(size);
    for (var cursor = 0, j = 0; j < buffers.length; cursor += buffers[j].length, j++) {
        buffers[j].copy(concatenated, cursor);
    }
    return concatenated;
}